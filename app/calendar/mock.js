function generateData(weekNum) {
  const list = [];
  for (let i = 1; i <= weekNum; i++) {
    const week = {
      date: `2023å¹´ç¬¬${i}å‘¨`,
      activities: [],
    };
    for (let j = 1; j <= 7; j++) {
      const dayOfWeek = days[j - 1];
      const activity = {
        type: getRandomCategory(),
        time: `${getDate(2023, i, j)} ${dayOfWeek} ${getRandomTime()}`,
        content: getRandomContent(),
      };
      week.activities.push(activity);
    }
    list.push(week);
  }
  return list;
}

